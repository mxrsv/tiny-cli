use std::path::{Path, PathBuf};

use anyhow::Result;

use super::{dev_search_roots, execute_per_item, is_idle, CleanProvider};
use crate::commands::clean::fs_safe::{dir_size_safe, walk_with};
use crate::commands::clean::types::{CleanItem, ExecAction, ExecReport, RiskLevel};

const ID: &str = "node-modules";
const LABEL: &str = "node_modules (idle)";

pub struct NodeModules {
    pub idle_days: u64,
    pub search_roots: Vec<PathBuf>,
}

impl NodeModules {
    pub fn new(idle_days: u64) -> Self {
        Self {
            idle_days,
            search_roots: dev_search_roots(),
        }
    }
}

impl CleanProvider for NodeModules {
    fn id(&self) -> &'static str {
        ID
    }
    fn label(&self) -> &'static str {
        LABEL
    }
    fn risk(&self) -> RiskLevel {
        RiskLevel::Review
    }
    fn available(&self) -> bool {
        !self.search_roots.is_empty()
    }
    fn discover(&self) -> Result<Vec<CleanItem>> {
        let mut items = Vec::new();
        for root in &self.search_roots {
            for found in find_node_modules(root, self.idle_days) {
                let size = dir_size_safe(&found);
                items.push(CleanItem {
                    category_id: ID.to_string(),
                    category_label: LABEL.to_string(),
                    path: found,
                    size,
                    risk: RiskLevel::Review,
                });
            }
        }
        Ok(items)
    }
    fn execute(&self, items: &[CleanItem], action: ExecAction) -> Result<ExecReport> {
        execute_per_item(items, action, ID)
    }
}

/// Walks `root` symlink-safe and returns every `node_modules` dir whose
/// parent has a `package.json` modified more than `idle_days` days ago.
/// Does NOT descend into a `node_modules` dir once found.
pub fn find_node_modules(root: &Path, idle_days: u64) -> Vec<PathBuf> {
    let mut found: Vec<PathBuf> = Vec::new();
    walk_with(root, |path, meta| {
        if !meta.file_type().is_dir() {
            return false;
        }
        let is_node_modules = path.file_name().and_then(|n| n.to_str()) == Some("node_modules");
        if is_node_modules {
            if is_idle_node_modules(path, idle_days) {
                found.push(path.to_path_buf());
            }
            // Never recurse into node_modules itself (avoid nested transitive dirs).
            return false;
        }
        true
    });
    found
}

/// True iff `nm` is `<parent>/node_modules` and `<parent>/package.json`
/// exists and is idle.
pub fn is_idle_node_modules(nm: &Path, idle_days: u64) -> bool {
    let parent = match nm.parent() {
        Some(p) => p,
        None => return false,
    };
    let manifest = parent.join("package.json");
    if !manifest.is_file() {
        return false;
    }
    is_idle(&manifest, idle_days)
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::commands::clean::providers::known_category_ids;
    use std::fs;

    fn tempdir(label: &str) -> PathBuf {
        let mut base = std::env::temp_dir();
        base.push(format!(
            "tiny-clean-nm-{}-{}",
            label,
            std::time::SystemTime::now()
                .duration_since(std::time::UNIX_EPOCH)
                .unwrap()
                .as_nanos()
        ));
        fs::create_dir_all(&base).unwrap();
        base
    }

    #[test]
    fn node_modules_id_in_known_categories() {
        assert!(known_category_ids().contains(&ID));
    }

    #[test]
    fn refuses_node_modules_without_package_json() {
        let root = tempdir("orphan");
        let proj = root.join("ghost");
        fs::create_dir_all(proj.join("node_modules")).unwrap();
        // No package.json → must not be flagged.
        let found = find_node_modules(&root, 0);
        assert!(found.is_empty(), "got: {:?}", found);
        let _ = fs::remove_dir_all(&root);
    }

    #[test]
    fn idle_threshold_filters_recently_modified() {
        let root = tempdir("idle");
        let proj = root.join("alive");
        fs::create_dir_all(proj.join("node_modules")).unwrap();
        let manifest = proj.join("package.json");
        fs::write(&manifest, b"{}").unwrap();
        // Just-touched manifest with idle_days=30 → must NOT be flagged.
        let found = find_node_modules(&root, 30);
        assert!(found.is_empty(), "fresh package.json should not flag");
        // Backdate manifest 31 days → idle_days=30 must now flag it.
        let old = std::time::SystemTime::now()
            - std::time::Duration::from_secs(31 * 86_400);
        std::fs::File::open(&manifest)
            .unwrap()
            .set_modified(old)
            .unwrap();
        let found_old = find_node_modules(&root, 30);
        assert_eq!(found_old.len(), 1, "31-day-old manifest should flag");
        let _ = fs::remove_dir_all(&root);
    }

    #[test]
    fn does_not_descend_into_node_modules() {
        // Nested node_modules/foo/node_modules must not produce 2 entries.
        let root = tempdir("nested");
        let proj = root.join("p");
        let nm = proj.join("node_modules");
        let inner = nm.join("foo").join("node_modules");
        fs::create_dir_all(&inner).unwrap();
        let manifest = proj.join("package.json");
        fs::write(&manifest, b"{}").unwrap();
        let old = std::time::SystemTime::now()
            - std::time::Duration::from_secs(40 * 86_400);
        std::fs::File::open(&manifest)
            .unwrap()
            .set_modified(old)
            .unwrap();
        let found = find_node_modules(&root, 30);
        assert_eq!(found.len(), 1);
        assert_eq!(found[0], nm);
        let _ = fs::remove_dir_all(&root);
    }
}
